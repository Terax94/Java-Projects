package symboltype;
public class SymbolType {
    public static void main(String[] args) {
        // Declare a variable
        char symbol = 'a';
        // Print the result ot the console
        //(int) synbol pokazva simvoka kato chislo
        System.out.println("The code of '" + symbol + "' is: " + (int) symbol);
        symbol = 'b';
        System.out.println("The code of '" + symbol + "' is: " + (int) symbol);
        symbol = 'A';
        System.out.println("The code of '" + symbol + "' is: " + (int) symbol);
    }
    
}
